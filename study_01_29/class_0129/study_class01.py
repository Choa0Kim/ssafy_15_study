# - 내부요인(코드에서)
# - 코드 흐름에서 조건 분기
# - 예상 가능한 버그 or 예외만 if -else로 잡음

# try-catch
# - 외부 요인
#   - 대표케이스: 파일 입출력 -> 파일이 존재하지 않음.
# - 발생 빈도가 낮은 문제들
# - 실패시에 대안 처리 필요
#   - 예시)파일이 없다면 하드코딩된 코드로 실행

# 외/내부 요인 모두  try-catch만 남발하면
#  1. 코드가 매우 길어짐
#  2. 속도가 느려짐
#   - 우선 돌려보자! 라는 접근법이기 때문에 느려짐.
