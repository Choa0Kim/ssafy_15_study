# 다음에 그 개수 만큼의 과목들 번호가 주어진다.
# 한 줄에 주어지는 번호에 중복은 없다.
# [출력]
# 출력의 각 줄은 ‘#x’로 시작하고, 공백을 한 칸 둔 다음 모든 과목을 완료하는데 필요한 최소 학기 수를 출력한다.
