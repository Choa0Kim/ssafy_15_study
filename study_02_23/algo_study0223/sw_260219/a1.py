# 선수 과목이 없는 과목은 첫 학기에 완료된다
# 모든 과목을 완료하는 데 몇 학기가 필요한지 계산하라.
# 모든 과목을 완료하는 것이 불가능할 수도 있음에 주의하라.
# [제약사항]
# 1.     과목의 개수 N은 2 이상 100 이하이다. (2 ≤ N ≤ 100)
# 2.     과목들의 선수과목 개수의 총 합은 1 이상 120 이하이다.
# 3.     선수 과목의 개수가 0인 과목이 하나도 없을 수도 있다.
