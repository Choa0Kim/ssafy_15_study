# 당신의 학과에는 N개의 필수 과목들이 있다.
# 과목들은 1번부터 N번까지 번호가 붙어 있다.
# 한 학기에는 가능한 모든 과목을 한꺼번에 들을 수 있
# 각 과목에는 선수 과목의 번호들이 있어서 선수 과목들 중 하나라도 이전 학기까지 완료하지 못한 경우는 해당 과목을 실패한다.
