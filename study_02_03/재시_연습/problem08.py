# 입력을 받기위한 input 함수는 절대 사용하지 않습니다.
def find_second_largest(numbers):
    # 정렬된 numbers 리스트 만들기
    sort_nums = sorted(numbers)
    arr = []
    # 빈리스트 arr에 중복없이 num 담기
    for num in sort_nums:
        if num not in arr:
            arr.append(num)
    # 중복없이 정렬된 리스트에서 뒤에서 두번째(두번째로 큰) 값 반환
    return arr[-2]        





